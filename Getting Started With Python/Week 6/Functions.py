# Functions

# We create a new function in Python by using the "def" keyword followed by optional parameters in parentheses.
# We indent the body of the function (using 4 spaces).
# This defines the function but does not execute the body of the function.

# Again the "def" statement does not automatically run the function. You must invoke it.

def print_lyrics():
    print("I am a Lumberjack, and I am okay.")
    print("I sleep all night and I work all day.")
    return

def main():
    print("Hello World!")
    print_lyrics()


if __init__ == "__main__":
    main()
