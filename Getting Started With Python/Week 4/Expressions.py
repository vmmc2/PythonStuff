# Constants
# Constants are fixed values such as numbers, letters and strings are called 'constants' because their value does not change.
# Numeric constants are as you expect.
# String constants can use single quotes ('') or double quotes (""). The same applies to letter constants.

greeting = "Hello World"
print(greeting) #it is going to print the string "Hello World"


